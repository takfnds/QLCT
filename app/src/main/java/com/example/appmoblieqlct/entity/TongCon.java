package com.example.appmoblieqlct.entity;

public class TongCon {
    int tc,tt,ns;

    public TongCon() {
    }

    public TongCon(int tc, int tt, int ns) {
        this.tc = tc;
        this.tt = tt;
        this.ns = ns;
    }

    public int getTc() {
        return tc;
    }

    public int getTt() {
        return tt;
    }

    public int getNs() {
        return ns;
    }
}
